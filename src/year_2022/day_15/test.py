from day_15 import process


def test_merge_overlapping_intervals_equal() -> None:
    input_ = [[2, 6], [1, 3], [8, 10], [10, 12], [10, 13]]
    assert process.merge_overlapping_intervals(input_) == [[1, 6], [8, 13]]


